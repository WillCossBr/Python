#Função isinstance():
#Compara se o conteúdo de uma variável é de determinado tipo.

x = 'Python'
y = 123

print("-"*30)

#o conteúdo de x é uma string? TRUE
print(isinstance(x, (str)))

# o conteúdo de y é um float? FALSE
print(isinstance(y, (float)))

print("-"*30)

